# Q01: While Basic
# 1から3まで数値を表示する
i = 1 #1を変数iに代入する
while i <= 3: #iが3以下の間繰り返す
    print(i) #iを表示する
    i += 1 #iを1増やす

# Q02: While Counter
# 0から4まで数値を表示する
count = 0 #0を変数countに代入する
while count < 5: #countが5未満の間繰り返す
    print(count) #countを表示する
    count += 1 #countを1増やす

# Q03: While Condition
# "Hello"を3回表示する
times = 0 #0を変数timesに代入する
while times < 3: #timesが3未満の間繰り返す
    print("Hello") #"Hello"を表示する
    times += 1 #timesを1増やす

