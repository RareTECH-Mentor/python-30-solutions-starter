# Q01: List Basic
# リストを作成して表示する
numbers = [1, 2, 3] #リストを作成する
print(numbers) #リストを表示する

# Q02: List Access
# リストの要素にアクセスして表示する
numbers = [1, 2, 3] #リストを作成する
print(numbers[1]) #インデックス1の要素（2）を表示する

# Q03: List Operations
# リストの要素を変更して表示する
numbers = [1, 2, 3] #リストを作成する
numbers[2] = 4 #インデックス2の要素を4に変更する
print(numbers) #変更後のリストを表示する

